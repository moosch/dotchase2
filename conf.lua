function love.conf(t)
  t.title = "dotchase2"     -- The title the window will have (string)
  t.version = "11.3"       -- The LOVE version the game is made for (string)
  t.console = true         -- Attach a console (boolean - Windows only)
  t.window.width = 1280    -- The window width
  t.window.height = 720    -- The window height
  t.window.vsync = 0       -- Disable vsync
end
